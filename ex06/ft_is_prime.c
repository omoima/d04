int 	ft_is_prime(int nb)
{
	int 	i;

	i = 1;
	if (nb <= 0){
		return 0;
	}
	else if (nb == 1){
		return 1;
	}
	else {
		i++;
		while (i <= nb){
			if ((nb % i == 0)&&(i==nb)){
				return 1;
			}
			else if((nb % i == 0)&&(i != nb)){
				return 0;
			}
			i++;
		}
	}
	return 0;	
}
